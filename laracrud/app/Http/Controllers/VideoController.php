     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
//        return $request->all();
        $video = new Video;
        $video->video_mode = request('video_mode');
        $video->video_row = request('video_row');
        $video->margin = request('margin');
        $video->hover = request('hover');
        $video->padding = request('padding');
        $video->save();

        return redirect('/video')->with('success', 'Data saved!');
    }

